// Primeiro exercício de lógica - ADS
// Objetivo: Verificar se um estudante foi aprovado

let nomeEstudante = "Daielle Gonzaga";
let nota1 = 8.5;
let nota2 = 7.0;
let media = (nota1 + nota2) / 2;

console.log("Estudante: " + nomeEstudante);
console.log("Média final: " + media);

if (media >= 7) {
    console.log("Status: Aprovada! Pronto para o próximo desafio.");
} else {
    console.log("Status: Em recuperação. Precisa estudar mais um pouco.");
}

